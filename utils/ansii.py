def mirror_ascii_art(art):
  """
  Зеркально отражает ASCII-арт по вертикали (левую и правую части меняет местами)
  
  :param art: Многострочная строка с ASCII-артом
  :return: Зеркально отраженный ASCII-арт
  """
  # Разбиваем арт на строки
  lines = art.split('\n')
  mirrored = [line[::-1] for line in lines]
  
  # Собираем обратно в многострочную строку
  return '\n'.join(mirrored)
